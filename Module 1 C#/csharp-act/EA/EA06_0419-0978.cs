using System;
namespace VarTypeProg {
	class VarType {
		public static void Main(string[] args) {
			int INT1 = 5, INT2 = 10;
			float FLOAT = 12.5f;
			string STR = "C# Programming is Cool!";
			System.Console.WriteLine("Int1: {0}", INT1);
			System.Console.WriteLine("Int2: {0}", INT2);
			System.Console.WriteLine("Float: {0}", FLOAT);
			System.Console.WriteLine("String: {0}", STR);
		}
	}
}
